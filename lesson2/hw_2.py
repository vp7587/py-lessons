# 7. Виведення числа в стовпчик
#
number = int(input("Введите целое 4х значное число для выведения в столбик всех его разрядов:\n"))
rank_4, rest_4 = divmod (number , 1000)
rank_3, rest_3 = divmod (rest_4 , 100)
rank_2, rest_2 = divmod (rest_3, 10)
rank_1 = rest_2 % 10

print(f"Четвертый разряд числа {number}: {rank_4}")
print(f"Третий разряд числа {number}: {rank_3}")
print(f"Второй разряд числа {number}: {rank_2}")
print(f"Первый разряд числа {number}: {rank_1}")
