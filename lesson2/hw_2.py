# 2. “Середнє трьох чисел”
#
print("Необходимо по очереди ввести 3 целых числа для вычисления среднеарифметического")
number_1 = int(input("Введите 1 число:\n"))
number_2 = int(input("Введите 2 число:\n"))
number_3 = int(input("Введите 3 число:\n"))
print(f"Вы ввели: {number_1},{number_2},{number_3}. Среднеарифметическоге от введеных чисел: {(number_1 + number_2 + number_3)/3}")
