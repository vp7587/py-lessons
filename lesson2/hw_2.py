# 3. “Перетворення хвилин у години”
#
minutes_raw = int(input("Введте целое число минут для приведения к виду: N часов M минут\n"))
hours, minutes = divmod(minutes_raw, 60)
#
print(f"Вы ввели: {minutes_raw}. Отформатированные данные: {hours} час. {minutes} мин.")
